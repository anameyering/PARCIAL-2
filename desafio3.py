#LISTA NOMES

#Mostra uma mensagem dizendo para o usuário escrever os nomes e agurda a resposta, após receber os nomes, eles são divididos em partes, conforme o usuário separou por vírgulas.
nomes = input("Digite os 5 nomes separados por vírgula: ").split(",")

#Logo após, aparecerá a lista de nomes na tela. 
print(nomes)
